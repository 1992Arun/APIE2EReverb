package org.postRequest;

public class Domestic {
	
	private String price;
	
	private String region;
	
	public void setPrice(String string) {
		this.price = string;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getPrice() {
		return price;
	}

	public String getRegion() {
		return region;
	}

	

}
